<h5>

Devo entregar o carro com tanque cheio?
</h5>
<p>As locadoras entregam o veículo com o tanque cheio, e o mesmo deve ser feito na devolução. Caso o carro seja devolvido sem estar reabastecido, a locadora cobrará o combustível baseando-se em uma tabela própria, geralmente com preços mais caros do que os praticados pelos postos de gasolina</p>
<h5>

Posso alugar o carro em uma cidade e devolver em outra?
</h5>
<p>
Algumas locadoras com lojas em ambos locais permitem que a devolução do carro seja feita em agência diferente da utilizada para retirada. No entanto, a depender do contrato, poderá ocorrer cobrança adicional, chamada de taxa de retorno/devolução.
</p>
<h5>

Preciso avisar a locadora caso haja mais de um condutor?
</h5>
<p>
A locadora deve ser informada no momento da contratação se mais de uma pessoa conduzirá o veículo. O motorista adicional precisa apresentar os documentos na retirada, bem como cumprir os mesmos requisitos exigidos ao titular da locação. Caso ocorra algum imprevisto e o condutor não esteja em contrato, o seguro não cobrirá os prejuízos, e serão cobrados integralmente do locatário.
